package jan07;

public class Operator
{
    public static void main(String[] args)
    {
        int p = 5;
        System.out.println(p);
        System.out.println(p++);
        System.out.println(p++);
        System.out.println(p++);
        System.out.println(--p);
        System.out.println(--p);
        System.out.println(--p);
        System.out.println(--p);
        System.out.println(--p);
    }
}
